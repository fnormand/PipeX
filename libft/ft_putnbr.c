#include "libft.h"

void	ft_putnbr(int i)
{
	if (i < 0)
		ft_putchar('-');
	if (i < 0)
		i = i * -1;
	if (i < 9)
		ft_putchar(i + 48);
	else
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
}
