#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		l;

	l = ft_strlen(s1);
	i = 0;
	while (s2[i] != '\0')
	{
		s1[i + l] = s2[i];
		i++;
	}
	s1[i + l] = 0;
	return (s1);
}
