#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char	*cp;

	cp = s1;
	while (n > 0)
	{
		if (*s2 != '\0')
			*cp++ = *(char *)s2++;
		else
			*cp++ = '\0';
		n--;
	}
	return (s1);
}
